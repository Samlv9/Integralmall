function Device() {
    /// <summary>
    /// 设备类型判定工具类。</summary>

    throw new Error("无法创建静态类型的实例。");
}


Device.isWindowsPhone = function isWindowsPhone () {
    /// <summary>
    /// 判断当前设备是否为 Windows Phone。</summary>

    return navigator.userAgent.indexOf("Windows Phone") >= 0;
}


Device.isAndroid = function isAndroid () {
    /// <summary>
    /// 判断当前设备是否为 Android。</summary>

    return !Device.isAndroid && navigator.userAgent.indexOf('Android') >= 0;
}


Device.isIOS = function isIOS () {
    /// <summary>
    /// 判断当前设备是否为 IOS。</summary>

    return !Device.isWindowsPhone() && /iP(ad|hone|od)/.test(navigator.userAgent);
}