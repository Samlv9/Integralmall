var HTMLEvent = derive(Event, function HTMLEvent( type, origin, bubbles, cancelable ) {
    /// <summary>
    /// DOM 事件桥接类型事件。</summary>
    Event.call(this, type, bubbles, cancelable);

    this._origin = origin;
});


Object.defineProperties(HTMLEvent.prototype, {
    /// <field type='DOMEvent'>
    /// 获取原始的 DOM Event 对象。</field>
    origin: {
        get: function () { return this._origin; }
    }
});