/// TODU:
/// 这里在 `document` 上取消 `touchmove` 默认行为，防止浏览器出现弹性滚动条。
document.addEventListener("touchmove", function disableMovement( evt ) {
    /// 启用自定义的滚动行为。
    evt.preventDefault();
});


document.addEventListener("touchstart", function disableBehavior( evt ) {
    evt.preventDefault();
});